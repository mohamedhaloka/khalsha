class LandShippingData {
  final String orderId,
      title,
      internationalShipping,
      goodsType,
      shippingType,
      fromCountryId,
      toCountryId,
      truckId,
      note,
      shipmentTypeId,
      wantStorage,
      storageDays,
      flammable,
      workers,
      content,
      loadingDate,
      deliveryDate,
      recipientName,
      recipientMobile;

  List<String> loading,
      loadingLat,
      loadingLng,
      delivery,
      deliveryLat,
      deliveryLng,
      descLoading,
      descDelivery,
      bundleName,
      bundleQuantity,
      bundleUnit,
      bundleTotalWeight,
      bundleImage,
      name,
      quantity,
      pack,
      unpack,
      packaging;

  LandShippingData({
    required this.orderId,
    required this.recipientMobile,
    required this.recipientName,
    required this.loading,
    required this.goodsType,
    required this.deliveryDate,
    required this.wantStorage,
    required this.note,
    required this.content,
    required this.title,
    required this.shipmentTypeId,
    required this.fromCountryId,
    required this.toCountryId,
    required this.storageDays,
    required this.delivery,
    required this.deliveryLat,
    required this.deliveryLng,
    required this.descDelivery,
    required this.descLoading,
    required this.flammable,
    required this.internationalShipping,
    required this.loadingDate,
    required this.loadingLat,
    required this.loadingLng,
    required this.shippingType,
    required this.truckId,
    required this.workers,
    required this.bundleUnit,
    required this.bundleTotalWeight,
    required this.bundleQuantity,
    required this.bundleName,
    required this.bundleImage,
    required this.name,
    required this.quantity,
    required this.pack,
    required this.packaging,
    required this.unpack,
  });

  Map<String, dynamic> toJson() => {
        'title': title,
        'international_shipping': internationalShipping,
        'goods_type': goodsType,
        'shipping_type': shippingType,
        if (internationalShipping == 'yes') ...{
          'fromcountry_id': fromCountryId,
          'tocountry_id': toCountryId,
        },
        if (goodsType == 'private_transfer') ...{
          'truck_id': truckId,
          'shipmenttype_id': shipmentTypeId,
          'workers': workers,
        },
        'want_storage': wantStorage,
        'storage_days': storageDays,
        'flammable': flammable,
        'note': note,
        'content': content,
        'loading_date': loadingDate,
        'delivery_date': deliveryDate,
        'recipient_name': recipientName,
        'recipient_mobile': recipientMobile,
      };
}
